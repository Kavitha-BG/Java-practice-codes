
public class FloatingDoublePrimitive {
	public static void main(String [] args) {
		float f1 = 2.718f;
		System.out.println("f1 = " + f1);
		
		float f2 = 2718e-3F;
		System.out.println("f2 = " + f2);
		
		double d1 = 123.4E306;
		System.out.println("d1 = " + d1);
		
		double d2 = 3.14876546636363;
		System.out.println("d2 = " + d2);
	}

}
