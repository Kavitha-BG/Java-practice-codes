public class ShortPrimitive {
	public static void main(String [] args) {
		short s1 = -32768;
		short s2 = 32767;
		short s3 = 500;
		
		System.out.println("b1 = " + s1);
		System.out.println("b2 = " + s2);
		System.out.println("b3 = " + s3);
		
//		short b5= -35000; //out of range
//		short b5 = 32768; //out of range
	}
}